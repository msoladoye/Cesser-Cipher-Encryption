    private void btnChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseFileActionPerformed
        // TODO add your handling code here:
        if(FileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION){
            txtaDisplay.setText("");
            try {
                myFile = FileChooser.getSelectedFile();
                fr = new FileReader(myFile);
                br = new BufferedReader(fr);
                lblPath.setText(myFile.toString());
                page = new String();
                try {
                    //int l = 0;
                    while ((line = br.readLine()) != null) {
                        //System.out.println("page Lenght: " + page.length());
                        page = (page + "\n" + line);
                        //System.out.println("page Lenght: " + page + " " + page.length());
                        //l++;
                        //System.out.println(page + " "+ l);
                        
                    }
                } catch (IOException ex) {
                    //Logger.getLogger(Encryptm.class.getName()).log(Level.SEVERE, null, ex);
                    System.err.println("File not Found");
                }
            } catch (FileNotFoundException ex) {
                //Logger.getLogger(Encryptm.class.getName()).log(Level.SEVERE, null, ex);
                System.err.println("Fille not found");
            }
            pageLenght = page.length();
            txtaDisplay.setText(page);
            characters = new char[pageLenght];
            for(int i = 0; i < characters.length; i++){
                characters[i] = page.charAt(i);
                txtaDisplay.setText(page);
                //System.out.println(characters[i]);
            }
        }
    }//GEN-LAST:event_btnChooseFileActionPerformed

    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        // TODO add your handling code here:
        try{
            shift = Integer.parseInt(txtShift.getText());
        }catch(NumberFormatException e){
            System.err.println("Choose a valid Number");
            return;
        }
        if(!txtShift.getText().equals("") && shift > 0 && shift < default_characters.length){
            txtaDisplay.setText("");
            new_characters = new char[characters.length];
            for(int i = 0; i < characters.length; i++){
                char_index =    getIndex(characters[i]);
                //int new_pos = char_index - shift;
                if(char_index < shift){
                    int new_pos = default_characters.length - (shift - char_index);
                    //System.out.println(i +" "+ char_index+ " " + (shift - char_index) +" "+ new_pos);
                    new_characters[i] = default_characters[new_pos];
                }
                else{
                    int new_pos = char_index - shift;
                    //System.out.println(i +" "+ char_index+ " " + shift +" "+ new_pos);
                    new_characters[i] = default_characters[new_pos];
                }
                //System.out.print(new_characters[i]);
                new_page += new_characters[i];
            }
            txtaDisplay.setText("");
            txtaDisplay.setText(new_page);
            System.out.println(new_page);
            
        }else{
            System.out.println("Invalid Shift Selection");
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

    private void btnDecryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecryptActionPerformed
        // TODO add your handling code here:
        try{
            shift = Integer.parseInt(txtShift.getText());
        }catch(NumberFormatException e){
            System.err.println("Choose a valid Number");
            return;
        }
        if(!txtShift.getText().equals("") && shift > 0 && shift < default_characters.length){
            txtaDisplay.setText("");
            new_characters = new char[characters.length];
            for(int i = 0; i < characters.length; i++){
                if(i > 1){
                    char_index = getIndex(characters[i]);
                    if((shift + char_index) > (default_characters.length - 1)){
                        int new_pos = (shift - 1) - ((default_characters.length - char_index) - 1);
                        //System.out.println(default_characters.length);
                        //System.out.println(i +" "+ char_index+ " " + shift +" "+ ((default_characters.length - char_index) - 1));
                        new_characters[i] = default_characters[new_pos];
                    }
                    else{
                        int new_pos = (shift) + char_index;
                        //System.out.println("default : "+default_characters.length);
                        //System.out.println(i +" "+ char_index+ " " + shift +" "+ (shift + char_index) + " "+new_pos);
                        new_characters[i] = default_characters[new_pos];
                    }
                    //System.out.print(new_characters[i]);
                    new_page += new_characters[i];
                }
            }
            txtaDisplay.setText("");
            txtaDisplay.setText(new_page);
            System.out.println(new_page);
        }else{
            System.out.println("Invalid Shift Selection");
        }
    }//GEN-LAST:event_btnDecryptActionPerformed

